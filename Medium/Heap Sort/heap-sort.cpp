// The functions should be written in a way that array become sorted 
// in increasing order when heapSort() is called.

class Solution
{
    public:
     void heapSort(int arr[], int n){
    priority_queue<int, vector<int>, greater<int> > pq;
    for(int i=0; i<n; i++){
        pq.push(arr[i]);
       }
        int i = 0;
        while(!pq.empty()){
            arr[i] = pq.top();
            pq.pop();
            i++;
        }
    }


};




